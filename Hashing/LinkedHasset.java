import java.util.*;
public class LinkedHasset {
    public static void main(String[] args) 
    {
     LinkedHashSet<String> lhs=new LinkedHashSet<>();
     lhs.add("sdfghj");
     lhs.add("wertyujnj");
     lhs.add("dfgbgyuj");
     lhs.add("gfh") ;
     for (String s : lhs) 
     {
      
        System.out.println(s);
     }  
    }
    
}
